public enum Event {

    /**
     * 订阅
     */
    SUBSCRIBE(1),

    /**
     * 退订
     */
    UNSUBSCRIBE(2),

    /**
     * 点击
     */
    CLICK(3),
    
    /**
     * 默认
     */
    NULL(0);

    private int code;

    private Event(int code) {
        this.code = code;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
